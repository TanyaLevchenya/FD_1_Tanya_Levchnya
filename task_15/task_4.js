'use strict'

let firstNum = +prompt();
let secondOperations = prompt();
let thirdNum = +prompt();

if (secondOperations == '-'){
        alert(firstNum - thirdNum);
}

if (secondOperations == '+'){
        alert(firstNum + thirdNum);
}

if (secondOperations == '/'){
        alert(firstNum / thirdNum);
}

if (secondOperations == '*'){
        alert(firstNum * thirdNum);
}


